#include"libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;

	i = 0;
	if (!f || !s)
		return (0);
	str = (char *)malloc(ft_strlen(s)+1);
	if (!str)
		return (0);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[ft_strlen(s)] = '\0';
	return (str);
}
